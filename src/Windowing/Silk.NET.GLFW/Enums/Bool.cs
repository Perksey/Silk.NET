namespace Silk.NET.GLFW
{
    /// <summary>
    /// Used where the GLFW Library expects GLFW_FALSE and GLFW_TRUE.
    /// </summary>
    public enum Bool
    {
        /// <summary>
        /// Use where the library would expect GLFW_FALSE.
        /// </summary>
        False = 0,

        /// <summary>
        /// Use where the Library would expect GLFW_TRUE.
        /// </summary>
        True = 1,
    }
}
